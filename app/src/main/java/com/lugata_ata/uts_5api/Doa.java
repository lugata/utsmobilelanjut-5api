package com.lugata_ata.uts_5api;

public class Doa {
    private String id;
    private String doa;
    private String ayat;
    private String latin;
    private String artinya;

    public String getId() {
        return id;
    }

    public String getDoa() {
        return doa;
    }

    public String getAyat() {
        return ayat;
    }

    public String getLatin() {
        return latin;
    }

    public String getArtinya() {
        return artinya;
    }
}
